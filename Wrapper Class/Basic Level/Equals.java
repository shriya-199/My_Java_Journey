// Write a program to check if two Integer objects have the same value using equals() method.
public class Equals 
{
    public static void main(String args[])
    {
        Integer i1= 5;
        Integer i2= 5;
        System.out.println("The values of i1 and i2 are: " + i1 + " and " + i2);
        System.out.println("The values of i1 and i2 are equal: " + i1.equals(i2));
    }
}
